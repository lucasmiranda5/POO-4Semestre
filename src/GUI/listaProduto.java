    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String id = idText.getText();
        if(id.isEmpty()){
           JOptionPane.showMessageDialog(null,"É necessario colocar o ID que quer editar","Error",JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                if(controller.excluir(id)){
                    try {
                        atualizarDados();
                    } catch (BancoDeDadosException ex) {
                        Logger.getLogger(listaProduto.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (SQLException ex) {
                        Logger.getLogger(listaProduto.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    JOptionPane.showMessageDialog(null,"Exclusão realizada com sucesso ","Sucesso",JOptionPane.DEFAULT_OPTION);
                }else{
                    JOptionPane.showMessageDialog(null,"Ocorreu algum erro ","Erro",JOptionPane.ERROR_MESSAGE);
                }} catch (BancoDeDadosException ex) {
                Logger.getLogger(listaProduto.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

