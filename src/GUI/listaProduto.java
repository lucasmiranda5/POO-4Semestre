    public void atualizarDados() throws BancoDeDadosException, SQLException{
        
        ArrayList<String[]> arr;
        arr = controller.listarTodos();
        DefaultTableModel aModel = (DefaultTableModel) tabelaLista.getModel();
        while(aModel.getRowCount()>0){
            aModel.removeRow(0);
        }
        int x;
        for(x = 0; x< arr.size(); x++){
            aModel.addRow(arr.get(x));
        }
        tabelaLista.setModel(aModel);   
        tabelaLista.revalidate();
    }
   

